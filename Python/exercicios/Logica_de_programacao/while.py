# Em Python, o loop While é utilizado para executar um bloco de código repetidamente enquanto uma condição é verdadeira.
# Para adicionar um comando break ao loop while, você pode modificar o código para incluir uma condição que permita sair do loop


contador = 0 

while contador < 100:
    print(f"o contador é {contador}")
    contador = contador + 1
    


print(f"aqui op contador {contador} acaba de chegar a 100, o loop while foi encerrado")